    private void but_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_deleteActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRow() != -1) {
            int showConfirmDialog = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to "
                    + "delete the selected supllier?", null, JOptionPane.YES_NO_OPTION);
            if (showConfirmDialog == JOptionPane.YES_OPTION) {
                try {
                    DBConnection.iud("update supplier set status='2' where id='" + jTable1.getValueAt(jTable1.getSelectedRow(), 0) + "'");
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
                loadTableData();
                JOptionPane.showMessageDialog(rootPane, "Supplier deleted successfully!");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a supplier from the table to delete.", null, JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_but_deleteActionPerformed


    private void but_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_searchActionPerformed
        if (!txt_name.getText().equals("")) {
            try {
                ResultSet search = DBConnection.search("select * from supplier where name like '%" + txt_name.getText() + "%'");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                Vector v;
                while (search.next()) {
                    v = new Vector();
                    v.add(search.getString("id"));
                    v.add(search.getString("name"));
                    v.add(search.getString("address"));
                    String stat = (search.getInt("status") == 1) ? "Active" : "Inactive";
                    v.add(search.getString("mobile"));
                    v.add(stat);
                    dtm.addRow(v);
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Type the supplier name to search.");
            txt_name.grabFocus();
        }
    }//GEN-LAST:event_but_searchActionPerformed


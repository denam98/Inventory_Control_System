    private void loadTableData() {
        try {
            ResultSet search = DBConnection.search("select * from supplier where status!=2");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            Vector v;
            while (search.next()) {
                v = new Vector();
                v.add(search.getString("id"));
                v.add(search.getString("name"));
                v.add(search.getString("address"));
                v.add(search.getString("mobile"));
                String stat = (search.getInt("status") == 1) ? "Active" : "Inactive";
                v.add(stat);
                dtm.addRow(v);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private boolean checkContents() {
        if (txt_name.getText().equals("")) {
            txt_name.grabFocus();
            contentChecked = false;
        } else if (txt_address.getText().equals("")) {
            txt_address.grabFocus();
            contentChecked = false;
        } else if (txt_mobile.getText().equals("") || txt_mobile.getText().length() != 10) {
            txt_mobile.grabFocus();
            contentChecked = false;
        } else if (!rb_active.isSelected() && !rb_inactive.isSelected()) {
            rb_active.grabFocus();
            contentChecked = false;
        } else {
            contentChecked = true;
        }
        return contentChecked;
    }

    private void addSupplier() {
        int stat = (rb_active.isSelected()) ? 1 : 0;
        try {
            DBConnection.iud("insert into supplier(name,address,mobile,status) values"
                    + "('" + txt_name.getText() + "','" + txt_address.getText() + "','" + txt_mobile.getText() + "','" + stat + "')");
            loadTableData();
            JOptionPane.showMessageDialog(rootPane, "Supplier added successfully!");
            clearFields();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }


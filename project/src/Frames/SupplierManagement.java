    private void but_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_updateActionPerformed
        if (checkContents() && updateProceder) {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int showConfirmDialog = JOptionPane.showConfirmDialog(rootPane, "Do you want to "
                    + "update the selected supplier data?", null, JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (showConfirmDialog == JOptionPane.YES_OPTION) {
                try {
                    int stat = (rb_active.isSelected()) ? 1 : 0;
                    DBConnection.iud("update supplier set name='" + txt_name.getText().toLowerCase() + "',"
                            + "address='" + txt_address.getText().toLowerCase() + "',mobile='" + txt_mobile.getText() + "',"
                            + "status='" + stat + "' where id='" + updatingId + "'");
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
                loadTableData();
                JOptionPane.showMessageDialog(rootPane, "Supplier data updated successfully!");
                clearFields();
                updateProceder = false;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select a supplier from the table and update contents first.");
        }
    }//GEN-LAST:event_but_updateActionPerformed


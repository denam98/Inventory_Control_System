    private void clearFields() {
        txt_item_name.setText(null);
        txt_buyprice.setText(null);
        txt_sellprice.setText(null);
        txt_qty.setText(null);
        lab_itemid.setText(null);
        txt_item_name.grabFocus();
    }

    double previousQty;

    private boolean checkAvailability() {
        String itemId = lab_itemid.getText().trim();
        int x = 0;
        boolean availability = false;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        while (x < jTable1.getRowCount()) {
            if (jTable1.getValueAt(x, 0).toString().equals(itemId)) {
                availability = true;
                previousQty = Double.valueOf(jTable1.getValueAt(x, 4).toString());
                dtm.removeRow(x);
                break;
            } else {
                availability = false;
            }
            x++;
        }
        return availability;
    }

    private void calculateGrnTotal() {
        int x = 0;
        double grnTotal = 0;
        while (x < jTable1.getRowCount()) {
            grnTotal += Double.parseDouble(jTable1.getValueAt(x, 5).toString());
            x++;
        }
        txt_grnTotal.setText(String.valueOf(grnTotal));
    }

    private void resetAll() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        txt_supp_name.setText(null);
        lab_suppID.setText(null);
        clearFields();
        dtm.setRowCount(0);
        txt_grnTotal.setText("0.0");
        txt_payment.setText("0.0");
        txt_balance.setText("0.0");
        txt_supp_name.grabFocus();
    }
    
    private boolean contentChecked;
    
    private boolean checkContents() {
        if (txt_supp_name.getText().equals("")) {
            txt_supp_name.grabFocus();
            contentChecked = false;
        } else if (txt_item_name.getText().equals("")) {
            txt_item_name.grabFocus();
            contentChecked = false;
        } else if (txt_buyprice.getText().equals("") || txt_buyprice.getText().length() == 0) {
            txt_buyprice.grabFocus();
            contentChecked = false;
        } else if (txt_sellprice.getText().equals("") || txt_sellprice.getText().length() == 0) {
            txt_sellprice.grabFocus();
            contentChecked = false;
        }else if (txt_qty.getText().equals("") || txt_qty.getText().length() == 0) {
            txt_qty.grabFocus();
            contentChecked = false;
        } else {
            contentChecked = true;
        }
        return contentChecked;
    }
    
    private void setImg(File f, JLabel lab){
        try {
            Image img = ImageIO.read(f);
            img = img.getScaledInstance(lab.getWidth(), lab.getHeight(), Image.SCALE_SMOOTH);
            lab.setIcon(new ImageIcon(img));
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }

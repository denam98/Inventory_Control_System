    private void txt_qtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_qtyActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();

        if (checkContents()) {
            if (checkAvailability()) {
                v.add(lab_itemid.getText());
                v.add(txt_item_name.getText());
                v.add(txt_buyprice.getText());
                v.add(txt_sellprice.getText());
                double qty = Double.valueOf(txt_qty.getText()) + previousQty;
                v.add(String.valueOf(qty));
                String itemTotal = String.valueOf(Double.parseDouble(txt_buyprice.getText()) * qty);
                v.add(itemTotal);
                dtm.addRow(v);
            } else {
                v.add(lab_itemid.getText());
                v.add(txt_item_name.getText());
                v.add(txt_buyprice.getText());
                v.add(txt_sellprice.getText());
                v.add(txt_qty.getText());
                String itemTotal = String.valueOf(Double.parseDouble(txt_buyprice.getText()) * Double.parseDouble(txt_qty.getText()));
                v.add(itemTotal);
                dtm.addRow(v);
            }
            clearFields();
            calculateGrnTotal();
        }
    }//GEN-LAST:event_txt_qtyActionPerformed

